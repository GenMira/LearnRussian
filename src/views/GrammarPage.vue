      <h2>過去形</h2>
      <br>
      <p>ロシア語の過去形は、現在形に比べると変化が圧倒的にシンプルです</p>
      <p>主語の人称に関係なく、主語の「性」と「数」だけで形が決まります</p>
      <br>
      <p>作り方としては動詞の不定形の末尾の「ть」を落として、別の語尾をつけます</p>
      <br>
