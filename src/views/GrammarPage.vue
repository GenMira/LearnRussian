      <br>
      <p>他の疑問詞としては、Куда&#769;(どこへ),Когда&#769;(いつ),Почему&#769;(なぜ),Как(どのように/どうやって)などがありますが、ここではその紹介だけに留めます
        <br>なぜならこれらを使う文を考えるときには動詞を使いたくなり、動詞を使った文を扱うためにはその前に性と数、格変化について扱わなければならないからです</p>
