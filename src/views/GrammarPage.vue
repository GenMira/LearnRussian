      <p>その代表例が中国語もです</p>
      <blockquote class="explain-sentence">
        <p>我爱你</p>
      </blockquote>
      <p>この文において、我は主語、你は目的語です。それぞれの役割を、主語は文の先頭、目的語は他動詞の後に置くことによってわかるようにしています</p>
      <p>そのため僕たちはこの文を見て、「<span style="color: red;">私は</span><span style="color: blue;">あなたを</span>愛しています」と読み取れるわけです</p>
      <br>
      <p>さて一方で<span style="color: red;">膠着語</span>は名詞に役割を示すための助詞をつけることによって役割を示します</p>
      <p>感の良い方はお気づきでしょう。日本語がその代表例です</p>
      <blockquote class="explain-sentence">
        <p>私はあなたを愛しています</p>
      </blockquote>
      <p>膠着語の特徴としては、助詞によって役割を示すため、位置をある程度自由に動かせる点にあります</p>
      <blockquote class="explain-sentence">
        <p>あなたを愛しています私は</p>
