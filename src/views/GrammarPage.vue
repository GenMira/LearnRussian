      <p>彼はどこですか？ — 彼は家にいます</p>
      <br>
      <p>чтоはものについて尋ねるとき、ктoは人や動物について尋ねるときに使います</p>
      <p>гдеは人や物が存在する（いる/ある）場所を尋ねるときに使います</p>
      <br>
      <p>疑問詞のある疑問文では疑問詞でイントネーションを上げ、それ以降はそのままです</p>
      <p>「『これ』は公園ですか？」の文のように発音します</p>
      <br>
      <p>онаは「彼女」を意味する代名詞で、英語で言えばsheです</p>
      <p>彼の場合はонになります</p>
      <p>
        詳しくは
