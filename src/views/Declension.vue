    <br>
    <p>形容詞には硬変化と軟変化があります</p>
    <p>硬変化は語幹が硬母音で終わるもの、軟変化は語幹が軟音で終わるものです</p>
    <p>硬変化では語尾に硬母音、軟変化では語尾に軟母音が使われます</p>
    <br>
    <p>形容詞の対格は男性単数と複数において、形容詞がつく名詞が活動体名詞か不活動体名詞かによって変わります</p>
    <br>
    <h3>- 活動体名詞</h3>
    <p>男性単数 : 対格 = 生格</p>
    <p>複数 : 対格 = 生格</p>
    <br>
    <h3>- 不活動体名詞</h3>
    <p>男性単数 : 対格 = 主格</p>
    <p>複数 : 対格 = 主格</p>
